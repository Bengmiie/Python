# 두부가게에서 두부 사오기
print('두부 가게에 간다.')

tofubox = 1                     # tofubox 의 값을 1로 대입
if tofubox > 0:                 # tofubox 에 대입한 값이 0보다 크다면
    print('두부를 산다.')
else:                               # tofubox 에 대입한 값이 0보다 크지않은 경우
    print('순두부를 산다.')

 # if : 만약 "조건" 에 맞다면 작동
